let currentList = myArray; // Track what's currently displayed

function displayRecipes(recipesToShow) {
  let accumulate = '';
  recipesToShow.forEach((recipe, index) => {
    accumulate += `
      <div class="holderAll">
        <div><img src="image/${recipe.image}"></div>
        <div>
          <p><b>Name:</b>${recipe.name}</p>
          <p><b> Author</b>:${recipe.author}</p>
          <p><b>Description:</b> <br>${recipe.description}
          <button id="show-btn-${index}" onclick="showAll(${index})">show more</button></p>
        </div>
      </div>
      <div class="show" id="show-${index}"></div>
    `;
  });

  document.querySelector('.display').innerHTML = accumulate;
  currentList = recipesToShow; // ✅ Save the list being shown (original or filtered)
}

