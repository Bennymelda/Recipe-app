let accumulator = '';
function showAll(index) {
  accumulator = '';
  let addUp = currentList[index]; // ✅ Use currentList instead of myArray
  let ingredientsHTML = addUp.ingredients.map(item => `<li>${item} </li>`).join('');
  accumulator += `
    <div class="together">
      <ul>
        <strong>Ingredients</strong>
        ${ingredientsHTML}
      </ul>
    </div>
    <div class="width">
      <strong>procedure:</strong>
      ${addUp.procedure}<button onclick="hideDetails(${index})">show less</button>
    </div>
  `;

  let contentDiv = document.querySelector(`#show-${index}`);
  contentDiv.innerHTML = accumulator;
  contentDiv.style.display = "block";

  let showBtn = document.querySelector(`#show-btn-${index}`);
  if (showBtn) {
    showBtn.style.display = 'none';
  }
}
//helps to hide the show button
function hideDetails(index) {
  let contentDiv = document.querySelector(`#show-${index}`);
  contentDiv.style.display = 'none';
  contentDiv.innerHTML = '';

  let showBtn = document.querySelector(`#show-btn-${index}`);
  if (showBtn) {
    showBtn.style.display = "inline";
  }
}
